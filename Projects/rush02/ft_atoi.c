int	ft_atoi(char *nbr)
{
	int	nb;
	int i;

	i = 0;
	nb = 0;

	while (nbr[i] != 0)
	{
		nb = 10 * nb + nbr[i] - '0';
		i++;
	}
	return (nb);
}

